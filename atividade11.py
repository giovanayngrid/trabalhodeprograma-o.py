i1=int(input('digite um número inteiro:'))
i2=int(input('digite outro número intero:'))
r1=float(input('digite um número real:'))
produto=(i1*2)*(i2/2)
soma=(i1*3)+r1
cubo=r1**3
print('o produto do dobro de {} multiplicado com metade de {} é {}.'.format(i1, i2, produto))
print('a soma do triplo de {} com o {} é de {}.'.format(i1, r1, soma))
print('{} elevado ao cubo é {}'.format(r1, cubo))
